// Generates src/beyond-bank.js: the pre-dealt "Beyond Evil" puzzle bank.
//
// A Beyond Evil puzzle is a regular Evil (qqwing Intermediate, 24-26 givens,
// >=3 pair-type steps, 180-degree symmetry, unique solution, never guessing)
// that ADDITIONALLY forces a hidden pair in a row or column within the first
// 3 placements - the "opening wall" that live generation can't afford to wait
// for (acceptance is ~1 in 2,500 candidates, so these are dealt from a bank).
//
// Reproducible: workers use fixed mulberry32 seeds and qqwing is pinned, so
// re-running this script regenerates the identical bank. The bank ORDER IS
// THE NUMBERING - "Beyond Evil Puzzle 7" is line 7. Never reorder or insert;
// append-only if the bank ever grows, or every puzzle number changes meaning.
//
//   node scripts/make-beyond-bank.mjs               master: spawn workers, write bank
//   node scripts/make-beyond-bank.mjs worker <id> <hits>   internal

import { fork } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import qqwing from 'qqwing'

const TARGET = 300
const WORKERS = 8

function mulberry32(seed) {
  return () => {
    let t = (seed += 0x6d2b79f5)
    t = Math.imul(t ^ (t >>> 15), t | 1)
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61)
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}

// One candidate; returns the 81-char givens string on a Beyond Evil hit
function attempt() {
  const qq = new qqwing()
  qq.setRecordHistory(true)
  qq.setPrintStyle(qqwing.PrintStyle.ONE_LINE)
  qq.generatePuzzleSymmetry(qqwing.Symmetry.ROTATE180)
  const puzzle = qq.getPuzzleString().replace(/[^1-9.]/g, '')
  qq.solve()
  if (qq.getDifficulty() !== qqwing.Difficulty.INTERMEDIATE) return null
  const clues = qq.getGivenCount()
  if (clues < 24 || clues > 26) return null
  if (qq.getHiddenPairCount() < 1) return null
  const pairs =
    qq.getNakedPairCount() +
    qq.getHiddenPairCount() +
    qq.getPointingPairTripleCount() +
    qq.getBoxLineReductionCount()
  if (pairs < 3) return null
  // the opening wall: a row/column hidden pair needed within 3 placements
  let placements = 0
  for (const line of qq.getSolveInstructionsString().split('\n')) {
    if (line.includes('Mark given')) continue
    if (/hidden pair in (row|column)/.test(line)) return placements <= 3 ? puzzle : null
    if (line.includes('Mark ')) placements++
    if (placements > 3) return null
  }
  return null
}

if (process.argv[2] === 'worker') {
  const [id, hits] = [Number(process.argv[3]), Number(process.argv[4])]
  Math.random = mulberry32((0xbe10d ^ Math.imul(id + 1, 0x9e3779b1)) >>> 0)
  let found = 0, tries = 0
  while (found < hits) {
    tries++
    const p = attempt()
    if (p) {
      found++
      process.send(p)
    }
  }
  process.stderr.write(`worker ${id}: ${hits} hits in ${tries} tries\n`)
  process.exit(0)
} else {
  const per = Math.ceil(TARGET / WORKERS)
  const buckets = Array.from({ length: WORKERS }, () => [])
  let done = 0, total = 0
  const t0 = Date.now()
  for (let id = 0; id < WORKERS; id++) {
    const w = fork(fileURLToPath(import.meta.url), ['worker', String(id), String(per)], {
      stdio: ['ignore', 'inherit', 'inherit', 'ipc'],
    })
    w.on('message', (p) => {
      buckets[id].push(p)
      total++
      if (total % 10 === 0) console.log(`${total}/${TARGET + (WORKERS * per - TARGET)} found, ${Math.round((Date.now() - t0) / 1000)}s`)
    })
    w.on('exit', (code) => {
      if (code !== 0) throw new Error(`worker ${id} exited ${code}`)
      if (++done < WORKERS) return
      // deterministic order: concatenate by worker id, then trim
      const bank = buckets.flat()
      if (new Set(bank).size !== bank.length) throw new Error('duplicate puzzle in bank')
      const out =
        '// Generated by scripts/make-beyond-bank.mjs - DO NOT EDIT, NEVER REORDER.\n' +
        '// The order is the numbering: "Beyond Evil Puzzle 7" is entry 7.\n' +
        'export default [\n' +
        bank.slice(0, TARGET).map((p) => `  '${p}',`).join('\n') +
        '\n]\n'
      writeFileSync(new URL('../src/beyond-bank.js', import.meta.url), out)
      console.log(`wrote src/beyond-bank.js: ${Math.min(bank.length, TARGET)} puzzles in ${Math.round((Date.now() - t0) / 1000)}s`)
    })
  }
}
